<h4><a href="#" onclick="showHide('#qaHead2Head')">Head 2 Head Challenge</a></h4>
<ul class="hidden" id="qaHead2Head">
	<li><b>I posted my challenge to my wall but why can't my friends view my
	challenge on Facebook?</b>
	This can happen if your app settings within Facebook are set to only allow
	limited audiences to view posts. To ensure maximum visibility, review
	your app privacy settings before submitting a post via SCHOOLD U to
	Facebook.</li>

	<li><b>Can I change the wager amount once I've created my challenge?</b>
	Currently, this option is not available for users. If you wish to change the
	challenge amount, please create a new challenge with the wager amount
	adjusted accordingly.</li>

	<li><b>What is the difference between a Private Challenge and a Public
	Challenge?</b>
	- A Private Challenge is a challenge that may only be viewed and
	accepted by people with the challenge URL.
	- A Public Challenge is a challenge that can be found and accepted by all
	SCHOOLD U users. Your challenge will be displayed on the <a href="openChallenges.php">Unmatched
	Challenges</a> page until a user accepts your challenge or your challenge is
	deleted.</li>

	<li><b>What if the friend I want to challenge does not accept my request?</b>
	If the specific person you wish to challenge does not accept your request,
	your challenge will remain as an Unmatched Challenge. Depending on if
	your challenge was created as a Private or Public Challenge, all friends
	with your challenge link can accept, or all SCHOOLD U users will see your
	challenge and can accept it.</li>

	<li><b>What if I have no one to challenge?</b>
	You can still get in on the game! Simply create a Head-to-Head Public
	Challenge or join the Pooled Challenge.</li>

	<li><b>How many challenges am I allowed to create and/or accept?</b>
	You can create and/or accept as many Head-to-Head Challenges as you
	would like.</li>
</ul>
